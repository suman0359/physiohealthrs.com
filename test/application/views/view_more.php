the incidence of obesity has more than
doubled in the past 30 years (104). This
sustained growth in the prevalence of
obesity suggests that current interventions
are ineffective; it is reasonable to expect
greater numbers of individuals to
experience the conditions associated with
excess body weight.
Significantly increased body weight is
considered to be a factor in heart disease,
diabetes, stroke and some types of cancer
(77, 99). Individuals with a body mass
index (BMI) of over 35 may face an increase
in mortality of 40% in females and 62% in
males, as compared to individuals with
BMI in the normal range (72). The obesity
epidemic is a potential threat to the
decades-long decline in American mortality
rates (77). Globally, overweight and obesity
comprise the fifth leading risk factor for
mortality, resulting in some 2.8 million
annual deaths (104).

